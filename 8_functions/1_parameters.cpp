// there are two types of parameters actual parameters and formal parameters.

void fun1(int num1, int num2){} // ----------> (1)

int main(){
    int num1=10, num2=100;  
    fun1(num1, num2); // ------------> (2)
}

// in the above piece of code (1) is called "Formal parameters"
// in the above piece of code (2) is called "actual parameters"